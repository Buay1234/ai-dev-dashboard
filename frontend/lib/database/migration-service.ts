export function migrationFilesToArtifacts(
  files: GeneratedSourceFile[],
  preview: string
): ProjectArtifact[] {
  const createdAt = new Date().toISOString();
  const artifacts: ProjectArtifact[] = files.map((f) => ({
    id: f.id,
    name: f.fileName,
    type: f.fileName.endsWith(".sql") ? "sql" : "csharp",
    content: f.content,
    agent: f.agent,
    createdAt,
  }));

  artifacts.push({
    id: "migration-preview",
    name: "MigrationPreview.md",
    type: "markdown",
    content: preview,
    agent: "Franky",
    createdAt,
  });

  return artifacts;
}

