export function generateMigrationFiles(
  entities: EntityDefinition[],
  databaseDesign?: DatabaseDesignContract | null
): GeneratedSourceFile[] {
  const fkByEntity = new Map<string, ForeignKeyDefinition[]>();
  if (databaseDesign) {
    for (const fk of databaseDesign.foreignKeys) {
      const list = fkByEntity.get(fk.entity) ?? [];
      list.push(fk);
      fkByEntity.set(fk.entity, list);
    }
  }

  const upOps = entities
    .map((entity) => {
      const fks = fkByEntity.get(entity.name) ?? [];
      const fkConstraints = fks
        .map(
          (fk) =>
            `                    table.ForeignKey(
                        name: "FK_${entity.tableName}_${fk.referencesEntity}",
                        column: x => x.${fk.property},
                        principalTable: "${entities.find((e) => e.name === fk.referencesEntity)?.tableName ?? fk.referencesEntity + "s"}",
                        principalColumn: "${fk.referencesProperty}");`
        )
        .join("\n");

      return `            migrationBuilder.CreateTable(
                name: "${entity.tableName}",
                columns: table => new
                {
${entity.fields.map((f) => `                    ${f.name} = table.Column<${mapEfType(f.csharpType)}>(nullable: ${!f.isRequired})`).join(",\n")}
                },
                constraints: table =>
                {
                    table.PrimaryKey("PK_${entity.tableName}", x => x.Id);
${fkConstraints}
                });`;
    })
    .join("\n\n");

  const downOps = entities
    .map((e) => `            migrationBuilder.DropTable(name: "${e.tableName}");`)
    .join("\n");

  const migrationCs = `using System;
using Microsoft.EntityFrameworkCore.Migrations;

#nullable disable

namespace ${PROJECT_NAMESPACE}.Infrastructure.Migrations;

/// <summary>
/// EF Core migration — generated by Franky (V24)
/// </summary>
public partial class InitialCreate : Migration
{
    protected override void Up(MigrationBuilder migrationBuilder)
    {
${upOps}
    }

    protected override void Down(MigrationBuilder migrationBuilder)
    {
${downOps}
    }
}
`;

  const designerCs = `using System;
using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Infrastructure;
using Microsoft.EntityFrameworkCore.Metadata;
using Microsoft.EntityFrameworkCore.Migrations;
using Microsoft.EntityFrameworkCore.Storage.ValueConversion;
using ${PROJECT_NAMESPACE}.Infrastructure.Data;

#nullable disable

namespace ${PROJECT_NAMESPACE}.Infrastructure.Migrations;

[DbContext(typeof(AppDbContext))]
[Migration("${MIGRATION_ID}")]
partial class InitialCreate
{
    protected override void BuildTargetModel(ModelBuilder modelBuilder)
    {
#pragma warning disable 612, 618
        modelBuilder.HasAnnotation("ProductVersion", "9.0.0");
${entities.map((e) => `        modelBuilder.Entity("${PROJECT_NAMESPACE}.Domain.Entities.${e.name}", b => { b.ToTable("${e.tableName}"); });`).join("\n")}
#pragma warning restore 612, 618
    }
}
`;

  const snapshotCs = `using System;
using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Infrastructure;
using Microsoft.EntityFrameworkCore.Metadata;
using Microsoft.EntityFrameworkCore.Storage.ValueConversion;
using ${PROJECT_NAMESPACE}.Infrastructure.Data;

#nullable disable

namespace ${PROJECT_NAMESPACE}.Infrastructure.Migrations;

[DbContext(typeof(AppDbContext))]
partial class AppDbContextModelSnapshot : ModelSnapshot
{
    protected override void BuildModel(ModelBuilder modelBuilder)
    {
#pragma warning disable 612, 618
        modelBuilder.HasAnnotation("ProductVersion", "9.0.0");
${entities.map((e) => `        modelBuilder.Entity("${PROJECT_NAMESPACE}.Domain.Entities.${e.name}", b => { b.ToTable("${e.tableName}"); });`).join("\n")}
#pragma warning restore 612, 618
    }
}
`;

  return [
    {
      id: "migration-initial",
      path: `${PROJECT_NAMESPACE}.Infrastructure/Migrations`,
      fileName: `${MIGRATION_ID}.cs`,
      category: "migration",
      agent: "Franky",
      language: "csharp",
      content: migrationCs,
    },
    {
      id: "migration-designer",
      path: `${PROJECT_NAMESPACE}.Infrastructure/Migrations`,
      fileName: `${MIGRATION_ID}.Designer.cs`,
      category: "migration",
      agent: "Franky",
      language: "csharp",
      content: designerCs,
    },
    {
      id: "migration-snapshot",
      path: `${PROJECT_NAMESPACE}.Infrastructure/Migrations`,
      fileName: "AppDbContextModelSnapshot.cs",
      category: "migration",
      agent: "Franky",
      language: "csharp",
      content: snapshotCs,
    },
  ];
}

