function mapEfType(csharpType: string): string {
  const t = csharpType.replace("?", "");
  const map: Record<string, string> = {
    int: "int",
    long: "long",
    bool: "bool",
    decimal: "decimal",
    double: "double",
    DateTime: "DateTime",
    Guid: "Guid",
    string: "string",
  };
  return map[t] ?? "string";
}

function propertyConfig(field: EntityDefinition["fields"][0]): string {
  if (field.name === "Name" && field.csharpType.includes("string")) {
    return 'builder.Property(x => x.Name).IsRequired().HasMaxLength(200);';
  }
  if (field.isRequired && field.csharpType.includes("string") && field.name !== "Name") {
    return `builder.Property(x => x.${field.name}).IsRequired();`;
  }
  if (field.name === "CreatedAt") {
    return 'builder.Property(x => x.CreatedAt).HasDefaultValueSql("GETUTCDATE()");';
  }
  return "";
}

export function generateEntityConfigurations(
  entities: EntityDefinition[],
  databaseDesign?: DatabaseDesignContract | null
): GeneratedSourceFile[] {
  return entities.map((entity) => {
    const fieldLines = entity.fields
      .map((f) => {
        const cfg = propertyConfig(f);
        return cfg ? `        ${cfg}` : "";
      })
      .filter(Boolean)
      .join("\n");

    const relationshipLines =
      databaseDesign?.fluentConfigurations
        .filter((c) => c.entity === entity.name)
        .map((c) => `        ${c.code.split("\n").join("\n        ")}`)
        .join("\n\n") ?? "";

    const relationshipBlock = relationshipLines
      ? `\n\n${relationshipLines}`
      : "";

    const content = `using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;
using ${PROJECT_NAMESPACE}.Domain.Entities;

namespace ${PROJECT_NAMESPACE}.Infrastructure.Data.Configurations;

/// <summary>
/// EF Core entity configuration — generated by Franky (V31)
/// </summary>
public class ${entity.name}Configuration : IEntityTypeConfiguration<${entity.name}>
{
    public void Configure(EntityTypeBuilder<${entity.name}> builder)
    {
        builder.ToTable("${entity.tableName}");
        builder.HasKey(x => x.Id);
${fieldLines}${relationshipBlock}
    }
}
`;

    return {
      id: `config-${entity.name}`,
      path: `${PROJECT_NAMESPACE}.Infrastructure/Data/Configurations`,
      fileName: `${entity.name}Configuration.cs`,
      category: "infrastructure",
      agent: "Franky",
      language: "csharp",
      content,
    };
  });
}

