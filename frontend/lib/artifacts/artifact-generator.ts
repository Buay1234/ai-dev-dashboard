export function generateProjectArtifacts(outputs: AgentOutputs): ProjectArtifact[] {
  const createdAt = new Date().toISOString();

  return ARTIFACT_SPECS.map((spec) => ({
    id: spec.id,
    name: spec.name,
    type: spec.type,
    agent: spec.agent,
    content: spec.build(outputs),
    createdAt,
  }));
}

export function getArtifactProgressSteps(
  artifacts: ProjectArtifact[]
): { id: string; label: string; done: boolean }[] {
  const labels: Record<string, string> = {
    "robin-requirement": "Requirements Complete",
    "robin-user-stories": "User Stories Complete",
    "zoro-api-spec": "API Specification Complete",
    "zoro-database": "Database Script Complete",
    "nami-ui-spec": "UI Specification Complete",
    "nami-screen-flow": "Screen Flow Complete",
    "franky-architecture": "Architecture Complete",
    "franky-technical-design": "Technical Design Complete",
    "usopp-test-cases": "Test Cases Complete",
    "usopp-test-checklist": "Test Checklist Complete",
  };

  const doneIds = new Set(artifacts.map((a) => a.id));
  return ARTIFACT_SPECS.map((spec) => ({
    id: spec.id,
    label: labels[spec.id] ?? spec.name,
    done: doneIds.has(spec.id),
  }));
}

