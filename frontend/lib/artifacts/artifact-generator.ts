function extractSection(text: string, headings: string[]): string | null {
  for (const heading of headings) {
    const re = new RegExp(
      `(?:^|\\n)#+\\s*${escapeRe(heading)}[^\\n]*\\n([\\s\\S]*?)(?=\\n#+\\s|$)`,
      "i"
    );
    const match = text.match(re);
    if (match?.[1]?.trim()) return match[1].trim();
  }
  return null;
}

function escapeRe(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function extractSqlBlocks(text: string): string {
  const blocks: string[] = [];
  const fenced = text.matchAll(/```(?:sql)?\s*([\s\S]*?)```/gi);
  for (const match of fenced) {
    const block = match[1].trim();
    if (/create\s+table|alter\s+table|insert\s+into/i.test(block)) {
      blocks.push(block);
    }
  }
  if (blocks.length > 0) return blocks.join("\n\n");

  const lines = text.split("\n");
  const sqlLines: string[] = [];
  let inSql = false;
  for (const line of lines) {
    if (/^(create|alter|insert|drop)\s+/i.test(line.trim())) inSql = true;
    if (inSql) {
      sqlLines.push(line);
      if (line.trim().endsWith(";")) inSql = false;
    }
  }
  return sqlLines.join("\n").trim();
}

function wrapDocument(title: string, agent: string, body: string, fallback: string) {
  const content = body.trim() || fallback.trim();
  return `# ${title}

> Generated by **${agent}** · AI Dev Dashboard V22

${content}
`;
}

